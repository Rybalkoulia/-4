#include <iostream>
using namespace std;

int main() {
    int N;
    cout << "Введіть кількість секунд, що минули з початку доби: ";
    cin >> N;

    // Перевіряємо, чи кількість секунд невід'ємна
    if (N >= 0) {
        // Розрахунок кількості повних годин: 1 година = 3600 секунд
        int hours = N / 3600; 
        cout << "Кількість повних годин: " << hours << endl;
    } else {
        // Якщо ввід некоректний, повідомляємо про помилку
        cout << "Помилка: кількість секунд повинна бути невід'ємною." << endl;
    }

    return 0;
}
